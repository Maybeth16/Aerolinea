package codigofuente;

public class Personas {
public static String Nombre;
public static String Apellido;
public static String Pasaporte;
public static String Identificacion;
public static String Permiso;
public static String getNombre() {
	return Nombre;
}
public static void setNombre(String nombre) {
	Nombre = nombre;
}
public static String getApellido() {
	return Apellido;
}
public static void setApellido(String apellido) {
	Apellido = apellido;
}
public static String getPasaporte() {
	return Pasaporte;
}
public static void setPasaporte(String pasaporte) {
	Pasaporte = pasaporte;
}
public static String getIdentificacion() {
	return Identificacion;
}
public static void setIdentificacion(String identificacion) {
	Identificacion = identificacion;
}
public static String getPermiso() {
	return Permiso;
}
public static void setPermiso(String permiso) {
	Permiso = permiso;
}

}
